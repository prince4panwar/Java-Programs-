// Program to print the elements of an array present on odd position
class oddposition{
public static void main(String args[])
{
int arr[]={1,2,3,4,5};
for(int i=0;i<5;i++)
{
    if(arr[i]%2!=0){
    System.out.println(arr[i]);
    }
    else{
     System.out.println("");
    }
}


}
}