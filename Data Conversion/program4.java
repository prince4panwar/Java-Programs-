/*How to convert long to String*/


class LongToStringConversion { 

	
	public static void main(String args[]) 
	{ 

		
		long a = 999999999999L; 
System.out.println("Long value:"+a);
		// convert into String 
		String str = String.valueOf(a); 

		
		 

		System.out.println(str); 
	} 
}